package com.example.budgetku.model.object;

public class Overview {
    private Integer total_balance;
    private Integer total_income;
    private Integer total_expense;

    public Integer getTotal_balance() {
        return total_balance;
    }

    public void setTotal_balance(Integer total_balance) {
        this.total_balance = total_balance;
    }

    public Integer getTotal_income() {
        return total_income;
    }

    public void setTotal_income(Integer total_income) {
        this.total_income = total_income;
    }

    public Integer getTotal_expense() {
        return total_expense;
    }

    public void setTotal_expense(Integer total_expense) {
        this.total_expense = total_expense;
    }
}
